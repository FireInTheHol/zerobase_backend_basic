package Data_and_Opertaion;

public class Casting {

	public static void main(String[] args) {
		 	double a = 1.1;
		 	System.out.println(a);
	        double b = 1;
	        
	         
	        System.out.println(b);
	         
	       
	        
	        int e = (int) 1.1;
	        System.out.println(e);
	         
	        // 1 to String 
	        String f = Integer.toString(1);
	        System.out.println(f.getClass());

	}

}
